// HTML escape for inserting untrusted strings into innerHTML when full
// markdown rendering isn't appropriate (e.g. session titles).
export function escapeHtml(s) {
  return String(s)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;");
}
